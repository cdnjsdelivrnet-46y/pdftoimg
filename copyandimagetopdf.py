def slugify(text: str, max_len: int = 80) -> str:
    """Turn a title into a safe filename slug."""
    text = text.strip().lower()
    text = re.sub(r"[^\w\s-]", "",  text)   # remove special chars
    text = re.sub(r"[\s_-]+",  "_", text)   # spaces → underscores
    text = re.sub(r"^[-_]+|[-_]+$", "", text)
    return text[:max_len] or "untitled"


def extract_drive_file_id(url: str):
    """Extract file ID from any Google Drive URL."""
    for pat in [r"/file/d/([a-zA-Z0-9_-]+)", r"[?&]id=([a-zA-Z0-9_-]+)"]:
        m = re.search(pat, url)
        if m:
            return m.group(1)
    return None


def download_pdf_bytes(url: str) -> bytes:
    """Download a PDF via Google Drive API."""
    file_id = extract_drive_file_id(url)
    if not file_id:
        raise ValueError(f"Cannot extract Drive file ID from: {url}")
    buf = io.BytesIO()
    dl  = MediaIoBaseDownload(buf, drive_svc.files().get_media(fileId=file_id))
    done = False
    while not done:
        _, done = dl.next_chunk()
    return buf.getvalue()


def pdf_to_png_bytes(pdf_bytes: bytes, dpi: int = 150) -> bytes:
    """Convert first page of PDF → PNG bytes."""
    pages = convert_from_bytes(pdf_bytes, dpi=dpi, first_page=1, last_page=1)
    if not pages:
        raise ValueError("PDF returned no pages.")
    buf = io.BytesIO()
    pages[0].save(buf, format="PNG")
    return buf.getvalue()


def upload_to_drive(png_bytes: bytes, filename: str, folder_id: str) -> str:
    """Upload PNG to Drive, make public, return shareable link."""
    meta  = {"name": filename, "parents": [folder_id]}
    media = MediaIoBaseUpload(io.BytesIO(png_bytes), mimetype="image/png", resumable=False)
    f     = drive_svc.files().create(body=meta, media_body=media, fields="id").execute()
    fid   = f["id"]
    drive_svc.permissions().create(
        fileId=fid,
        body={"type": "anyone", "role": "reader"}
    ).execute()
    return f"https://drive.google.com/uc?id={fid}"


def get_or_create_sheet(spreadsheet, name: str, num_cols: int):
    """Return existing worksheet or create a new one."""
    try:
        return spreadsheet.worksheet(name)
    except gspread.exceptions.WorksheetNotFound:
        ws = spreadsheet.add_worksheet(title=name, rows=1000, cols=num_cols + 1)
        print(f"  ✦ Created new worksheet: '{name}'")
        return ws

