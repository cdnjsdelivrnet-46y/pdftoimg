def main():
    print("=" * 62)
    print("   Pending → Process → Done  |  PDF → Image → Drive → Sheet3")
    print("=" * 62)

    spreadsheet = gc.open_by_key(SPREADSHEET_ID)
    src_ws      = spreadsheet.worksheet(SOURCE_SHEET)
    all_rows    = src_ws.get_all_values()

    if not all_rows or len(all_rows) < 2:
        print("Sheet1 is empty or has no data rows.")
        return

    header    = all_rows[0]
    data_rows = all_rows[1:]   # row index offset: data_rows[i] → Sheet row i+2

    # ── Prepare Sheet3 ────────────────────────────────────────────
    dest_ws   = get_or_create_sheet(spreadsheet, DEST_SHEET, len(header))
    dest_data = dest_ws.get_all_values()

    if not dest_data:
        dest_ws.append_row(header + ["Image Link"])
        dest_data = [header + ["Image Link"]]

    # Build duplicate-check set from Sheet3 col B (title), lowercased
    processed_titles = {
        row[TITLE_COL_IDX].strip().lower()
        for row in dest_data[1:]
        if len(row) > TITLE_COL_IDX and row[TITLE_COL_IDX].strip()
    }
    print(f"\n  Already in Sheet3   : {len(processed_titles)} row(s)")

    # ── Scan Sheet1 for Pending rows ──────────────────────────────
    pending_rows = []
    for i, row in enumerate(data_rows):
        padded = row + [""] * (max(STATUS_COL_IDX, PDF_COL_IDX, TITLE_COL_IDX) + 1 - len(row))
        if padded[STATUS_COL_IDX].strip().lower() == STATUS_PENDING:
            pending_rows.append((i, padded))

    print(f"  Pending in Sheet1   : {len(pending_rows)} row(s)\n")

    if not pending_rows:
        print("  ✅ Nothing to process — no Pending rows found in Sheet1.")
        return

    done_count  = 0
    skip_count  = 0
    error_count = 0

    for i, row in pending_rows:
        sheet1_row_num = i + 2        # 1-indexed sheet row (header is row 1)
        title     = row[TITLE_COL_IDX].strip()
        title_key = title.lower()
        pdf_url   = row[PDF_COL_IDX].strip()

        print(f"  [ROW {sheet1_row_num}] '{title[:55]}'")

        # ── Duplicate guard ───────────────────────────────────────
        if title_key in processed_titles:
            print(f"    ⏭  Already in Sheet3 — skipping & marking Done.\n")
            # Still mark Done in Sheet1 so it doesn't reappear as Pending
            src_ws.update_cell(sheet1_row_num, STATUS_COL_IDX + 1, STATUS_DONE)
            skip_count += 1
            continue

        image_link = ""
        success    = True

        if pdf_url:
            try:
                print(f"    → Downloading PDF…")
                pdf_bytes = download_pdf_bytes(pdf_url)

                print(f"    → Converting to image…")
                png_bytes = pdf_to_png_bytes(pdf_bytes)

                filename  = slugify(title) + ".png"
                print(f"    → Uploading '{filename}' to Drive…")
                image_link = upload_to_drive(png_bytes, filename, DRIVE_FOLDER_ID)
                print(f"    ✓ Image: {image_link}")

            except Exception as e:
                print(f"    ✗ ERROR: {e}")
                image_link = f"ERROR: {e}"
                success    = False
                error_count += 1
        else:
            print(f"    ⚠  No PDF URL in col E — row copied without image.")

        # ── Copy row to Sheet3 ────────────────────────────────────
        dest_ws.append_row(list(row) + [image_link], value_input_option="USER_ENTERED")
        processed_titles.add(title_key)

        # ── Update Sheet1 status: Pending → Done ─────────────────
        # Always mark Done (even on PDF error) so the row isn't
        # retried endlessly. The error message is saved in Sheet3.
        src_ws.update_cell(sheet1_row_num, STATUS_COL_IDX + 1, STATUS_DONE)
        print(f"    ✓ Sheet1 row {sheet1_row_num} status updated → '{STATUS_DONE}'\n")

        done_count += 1

    # ── Summary ───────────────────────────────────────────────────
    print("=" * 62)
    print(f"  ✅ Processed & marked Done : {done_count}")
    print(f"  ⏭  Skipped (already done)  : {skip_count}")
    print(f"  ❌ Errors (see Sheet3)      : {error_count}")
    print("=" * 62)


main()